    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int [] sRows = jTableEntity.getSelectedRows();
        List <Person> entitiesToDel=new ArrayList<Person>();
        if (sRows.length > 0){
            for (int i=0; i<sRows.length; i++) {
                entitiesToDel.add(entityList.get(sRows[i]));
            }
            for (Person entity:entitiesToDel) AllPerson.getInstance().deleteEntity(entity);
            refreshFields();
        }        
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        int [] sRows = jTableEntity.getSelectedRows();
        if (sRows.length > 0){
            Person entity=entityList.get(sRows[0]);
            AllPersonUpdateDialog dialog= new AllPersonUpdateDialog( new javax.swing.JFrame(),entity, true);
            dialog.setVisible(true);
            AllPerson.getInstance().updateEntity(dialog.getEntity());
            refreshFields();
            jTableEntity.setRowSelectionInterval(sRows[0], sRows[0]);
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        AllPersonUpdateDialog dialog= new AllPersonUpdateDialog( new javax.swing.JFrame(), true);
        dialog.setVisible(true);
        if (dialog.getEntity()!=null) AllPerson.getInstance().addEntity(dialog.getEntity());
        refreshFields();
    }//GEN-LAST:event_jButtonInsertActionPerformed

