    public AllPersonDialog(java.awt.Frame parent, List<Person> entities, Person selectedEntity, boolean modal) {
        super(parent, modal);
        initComponents();
        this.entityList=entities;
        refreshFields();        
        if (selectedEntity!=null&&this.entityList!=null)
            if (selectedEntity.getId()!=null)
                for(int i=0; i<this.entityList.size(); i++) {
                    if (entityList.get(i).getId()!=null)
                    if (selectedEntity.getId().compareTo(entityList.get(i).getId())==0){
                        this.selectedEntity=selectedEntity;
                        jTableEntity.setRowSelectionInterval(i, i);
                    }
        }                
    }

    public AllPersonDialog(java.awt.Frame parent, List<Person> entities, int selectionIdx, boolean modal) {
        super(parent, modal);
        initComponents();
        this.entityList=entities;
        refreshFields();
        if (entityList!=null) 
            if (selectionIdx>0 && selectionIdx<entities.size()) 
                jTableEntity.setRowSelectionInterval(selectionIdx, selectionIdx);
                
    }

    
