    public void refreshTableTel(){
        if (entity==null) return;
        tableModelTel= new AllStringTableModel(entity.getTel(),"Telefon");
        jTableTel = new JTable(tableModelTel);
        jScrollPaneTel.setAutoscrolls(true);
        jScrollPaneTel.setViewportView(jTableTel);             
    }
    
    public void refreshTableEmail(){
        if (entity==null) return;
        tableModelEmail= new AllStringTableModel(entity.getEmail(),"Emails");
        jTableEmail = new JTable(tableModelEmail);
        jScrollPaneEmail.setAutoscrolls(true);
        jScrollPaneEmail.setViewportView(jTableEmail);             
    }

    public void refreshTableAddresse(){
        if (entity==null) return;
        List <String> sList=null;
        //if (entity.getAddress()==null) return;
        if (entity.getAddress()!=null) {
            sList= new ArrayList<String>();
            for (PersonAddress pa:entity.getAddress()) {
                sList.add(pa.printPerson("De"));
            }        
        }
        tableModelAddress= new AllStringTableModel(sList,"Adresse");
        jTableAddress = new JTable(tableModelAddress);
        jScrollPane3.setAutoscrolls(true);
        jScrollPane3.setViewportView(jTableAddress);             
    }
    
    
    public void setFields() {
        if (entity!=null) {
            if(entity.getId()!=null) jLabelId.setText("id: "+entity.getId());
            if(entity.getName()!=null) jTextFieldName.setText(entity.getName());
            if(entity.getId()!=null) jTextFieldDescription.setText(entity.getDescription());            
            MyHelpMethods4GUI.setJTextFeld(jTextFieldForename, entity.getForename());
            MyHelpMethods4GUI.setJTextFeld(jTextFieldTestimonal, entity.getTestimonal());
            MyHelpMethods4GUI.setJTextFeld(jTextFieldTitel, entity.getTitel());
            if (entity.getRole()!=null){
                String s=AllAdessoRole.getInstance().getEntityById(entity.getRole()).getName();
                MyHelpMethods4GUI.setJTextFeld(jTextFieldRolle, s);
            }
            refreshTableTel();
            refreshTableEmail();
            refreshTableAddresse();
        }
    }
    public void getFields() {
        if (entity==null) return;
        entity.setName(jTextFieldName.getText());
        entity.setForename(jTextFieldForename.getText());
        entity.setTitel(jTextFieldTitel.getText());
        entity.setDescription(jTextFieldDescription.getText());
        entity.setTestimonal(jTextFieldTestimonal.getText());        
    }

