    private void jButtonTelNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTelNewActionPerformed
        // TODO add your handling code here:
        String header="Neues Telefon anlegen";
        String description="Bitte fügen Sie ein neues Telefon ein";
        AllStringUpdateDialog dialog= new AllStringUpdateDialog( new javax.swing.JFrame(),null, header, description, true);
        dialog.setVisible(true);
        if (dialog.getEntity()!=null&&entity!=null) {
            if (entity.getTel()==null) entity.setTel(new ArrayList<String>());
            entity.getTel().add(dialog.getEntity());
        }
        refreshTableTel();        
    }//GEN-LAST:event_jButtonTelNewActionPerformed

